public class Ex05_09 {
    public static void main(String[] args) {

        int a = 100;

        if (a >= 90) {
            System.out.printf("A");

        } else if (a >= 80) {
            System.out.printf("B");

        } else if (a >= 70) {
            System.out.printf("C");

        } else if (a >= 60) {
            System.out.printf("D");

        } else {
            System.out.printf("F");

        }
        System.out.printf(" 학점 입니다. ");
    }
}
